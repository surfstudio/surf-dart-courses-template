void main() {
  List<AgriculturalMachinery> allMachineries = [];

  // Собираем все единицы техники в один список
  mapBefore2010.forEach((country, territories) {
    territories.forEach((territory) {
      allMachineries.addAll(territory.machineries);
    });
  });

  mapAfter2010.forEach((country, territories) {
    territories.forEach((territory) {
      allMachineries.addAll(territory.machineries);
    });
  });

  allMachineries.sort((a, b) => a.releaseDate.year - b.releaseDate.year);

  int totalMachineries = allMachineries.length;
  int halfTotal = (totalMachineries / 2).ceil();

  int sumAllAges = 0;
  int sumHalfAges = 0;

  for (int i = 0; i < totalMachineries; i++) {
    int age = DateTime.now().year - allMachineries[i].releaseDate.year;
    sumAllAges += age;

    if (i < halfTotal) {
      sumHalfAges += age;
    }
  }

  double avgAgeAll = sumAllAges / totalMachineries;
  double avgAgeHalf = sumHalfAges / halfTotal;

  print('Средний возраст всей техники: $avgAgeAll');
  print('Средний возраст 50% самой старой техники: $avgAgeHalf');
}

enum Countries { brazil, russia, turkish, spain, japan }

class Territory {
  int areaInHectare;
  List<String> crops;
  List<AgriculturalMachinery> machineries;

  Territory(
    this.areaInHectare,
    this.crops,
    this.machineries,
  );
}

class AgriculturalMachinery {
  final String id;
  final DateTime releaseDate;

  AgriculturalMachinery(
    this.id,
    this.releaseDate,
  );

  @override
  bool operator ==(Object? other) {
    if (other is! AgriculturalMachinery) return false;
    if (other.id == id && other.releaseDate == releaseDate) return true;

    return false;
  }

  @override
  int get hashCode => id.hashCode ^ releaseDate.hashCode;
}

